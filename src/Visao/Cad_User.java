    private void btnSalvarArtistaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarArtistaActionPerformed
        
            
            
        if (txtNomeArtista.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite o nome!", null, 0);
                txtNomeArtista.requestFocus();
            }else {

                
                try {
                    String Consulta;
                    int verificaNome =0;
                    conexao.getConexao();                 
                    Consulta = ("Select nome_artista from artista where nome_artista = '"+txtNomeArtista.getText()+"'");
                    Statement st = conexao.getConexao().createStatement();
                    ResultSet rs = st.executeQuery(Consulta);
                
                    while(rs.next()){                        
                        if(rs.getString("nome_artista") == null ? txtNomeArtista.getText() == null : rs.getString("nome_artista").equals(txtNomeArtista.getText())){
                            verificaNome = 1;
                        }                        
                    }
                    if(verificaNome == 0){                      
                        conexao.executa("insert into artista (nome_artista) values ('"+txtNomeArtista.getText()+"')");
                        cadMais = JOptionPane.showConfirmDialog(null, "Deseja cadastrar outro Artista?", "Sair", JOptionPane.YES_NO_OPTION);

                        if (cadMais == 1) {
                            this.setVisible(false);
                            this.dispose();
                        } else {
                            //this.setVisible(true);
                            limpaCamposArtista();
                            txtNomeArtista.requestFocus();
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "Artista já Existe!");
                        txtNomeArtista.requestFocus();
                    }
                } catch (Exception ex) {
                    Logger.getLogger(Cad_User.class.getName()).log(Level.SEVERE, null, ex);
                }
               
                
            }
    }//GEN-LAST:event_btnSalvarArtistaActionPerformed

