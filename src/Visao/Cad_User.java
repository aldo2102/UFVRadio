    private void btnSalvarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarFuncActionPerformed
        if (txtNomeFunc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite o nome!", null, 0);
                txtNomeFunc.requestFocus();
            }
            else if (txtCpfFunc.getText().compareTo("   .   .   -  ") == 0 || validador.isCPF(txtCpfFunc.getText().replace(".", "").replace("-", "")) == false) {
                JOptionPane.showMessageDialog(null, "CPF inválido!", null, 0);
                txtCpfFunc.requestFocus();
            }
            else if ((txtDataFunc.getText().compareTo("  /  /    ")) == 0) {
                JOptionPane.showMessageDialog(null, "Data de Nascimento inválida", null, 0);
                txtDataFunc.requestFocus();
            }
            else if (txtSalarioFunc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Sálario inválido!", null, 0);
                txtSalarioFunc.requestFocus();
            }
            
            else if(txtLogradouroFunc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite a Rua!");
                txtLogradouroFunc.requestFocus();
            }
            else if(txtNumeroFunc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite o número!");
                txtNumeroFunc.requestFocus();
            }
            else if(txtBairroFunc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite o Bairro!");
                txtBairroFunc.requestFocus();
            }
            else if(txtCidadeFunc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite a Cidade!");
                txtCidadeFunc.requestFocus();
            }
            else if(txtUsuarioFunc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite o Usuário!");
                txtUsuarioFunc.requestFocus();
            }
            else if(txtSenhaFunc.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite a Senha!");
                txtSenhaFunc.requestFocus();
            }
            else {

                try {
                    String Consulta;
                    int verificaCpf =0,verificaUsuario=0;
                    conexao.getConexao();                 
                    Consulta = ("Select cpf_func, usuario from funcionario where cpf_func = '"+txtCpfFunc.getText()+"'");
                    Statement st = conexao.getConexao().createStatement();
                    ResultSet rs = st.executeQuery(Consulta);
                
                    while(rs.next()){
                        if(rs.getString("cpf_func") == null ? txtCpfFunc.getText() == null : rs.getString("cpf_func").equals(txtCpfFunc.getText())){
                            verificaCpf=1;
                        } 
                        if(rs.getString("usuario") == null ? txtUsuarioFunc.getText() == null : rs.getString("usuario").equals(txtUsuarioFunc.getText())){
                            verificaUsuario=1;
                        }
                    }
                    
                   
                    if(verificaCpf==0 && verificaUsuario ==0){
                        /*StringBuilder data = new StringBuilder();
                        SimpleDateFormat dataBD = new SimpleDateFormat("yyyy-MM-dd");
                        //o primeiro tem o padrao que será informado:  
   
                        //Para converter de String para Date:    
                        java.util.Date date = dataBD.parse(txtDataFunc.getText());  
                        data.append(dataBD.format(date));
                        JOptionPane.showMessageDialog(null, data.toString());*/
                        conexao.executa("insert into funcionario (cpf_func,nome_func,dt_nasc,cargo,salario,usuario,senha,logradouro,numero,bairro,cidade,estado) values ('"+txtCpfFunc.getText()+"','"+txtNomeFunc.getText()+"','1993-09-27','Locutor','"+txtSalarioFunc.getText()+"','"+txtUsuarioFunc.getText()+"','"+txtSenhaFunc.getText()+"','"+txtLogradouroFunc.getText()+"','"+txtNumeroFunc.getText()+"','"+txtBairroFunc.getText()+"','"+txtCidadeFunc.getText()+"','"+comboEstadoFunc.getSelectedItem()+"')");
                        cadMais = JOptionPane.showConfirmDialog(null, "Deseja cadastrar outro usuario?", "Sair", JOptionPane.YES_NO_OPTION);

                        if (cadMais == 1) {
                            this.setVisible(false);
                            this.dispose();
                        } else {
                            //this.setVisible(true);
                            limpaCampos();
                            txtNomeFunc.requestFocus();
                        }
                    }else if(verificaCpf == 1){                        
                        JOptionPane.showMessageDialog(null, "Cpf já Cadastrado!");
                        txtCpfFunc.requestFocus();
                    }else if(verificaUsuario == 1){                        
                        JOptionPane.showMessageDialog(null, "Usuário já Existe!");
                        txtUsuarioFunc.requestFocus();
                    }
                     conexao.desconect();
                } catch (Exception ex) {
                    Logger.getLogger(Cad_User.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                
            }
        
        
    }//GEN-LAST:event_btnSalvarFuncActionPerformed

